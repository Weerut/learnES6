function add(n1, n2) {
    return n1 + n2;
}

function subtract(n1, n2) {
    return n1 - n2;
}

function division(n1, n2) {
    return n1 / n2;
}

function multiply(n1, n2) {
    return n1 * n2;
}

function mod(n1, n2) {
    return n1 % n2;
}

var pi = 3.1415;

export {
    add, subtract, division, multiply, mod, pi
}